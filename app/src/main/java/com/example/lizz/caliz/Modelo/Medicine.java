public class Medicine {

    private String nombreMedicina;
    private String horaMedicina;
    private String diaMedicina;


    public String getNombreMedicina() {
        return nombreMedicina;
    }

    public void setNombreMedicina(String nombreMedicina) {
        this.nombreMedicina = nombreMedicina;
    }

    public String getHoraMedicina() {
        return horaMedicina;
    }

    public void setHoraMedicina(String horaMedicina) {
        this.horaMedicina = horaMedicina;
    }

    public String getDiaMedicina() {
        return diaMedicina;
    }

    public void setDiaMedicina(String diaMedicina) {
        this.diaMedicina = diaMedicina;
    }
}

